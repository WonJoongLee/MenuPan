package com.example.menupan.Adapter.SchoolRecyclerView;

public class Restaurant {
    private int image;
    private String name;

    public int getImage() {
        return image;
    }

    public String getName() {
        return name;
    }

    public Restaurant(int image, String name) {
        this.image = image;
        this.name = name;
    }
}
